def route_question(question: str) -> list[tuple[str, list[str]]]:
    """
    Match a Hebrew question to relevant ת"י standards.

    Returns: list of (display_name, [absolute_pdf_paths]) sorted by relevance score.
    Each entry represents one standard group.
    """
    q_lower = question.lower()

    scored: list[tuple[int, str, list[str]]] = []
    for display_name, rel_paths, keywords in _STANDARDS:
        score = 0
        for kw in keywords:
            if kw.lower() in q_lower:
                score += 1
                # Bonus for exact ת"י number mention
                if re.search(r'\b' + re.escape(kw) + r'\b', question):
                    score += 1

        if score > 0:
            # Convert to absolute paths, keep only existing files
            abs_paths = []
            for rp in rel_paths:
                full = _PROJECT_ROOT / rp
                if full.exists():
                    abs_paths.append(str(full))
            if abs_paths:
                scored.append((score, display_name, abs_paths))

    scored.sort(key=lambda x: x[0], reverse=True)
    return [(name, paths) for _, name, paths in scored]


def get_all_standards_summary() -> str:
    """Return a text summary of all available standards for display/system prompts."""
    lines = ["=== תקנים ישראליים זמינים (ת\"י) ===\n"]
    for display_name, rel_paths, keywords in _STANDARDS:
        kw_sample = ", ".join(keywords[:5])
        lines.append(f"• {display_name}")
        lines.append(f"  נושאים: {kw_sample}")
    return "\n".join(lines)
