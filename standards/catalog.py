def build_standards_reference() -> str:
    """Build a concise text block to embed in the system prompt."""
    lines = ["=== תקנים ישראליים (ת\"י) ומספרי סעיפים ==="]
    for category, entries in ISRAELI_STANDARDS.items():
        std = entries.get("_std", "")
        lines.append(f"\n[{category}] — {std}")
        for key, val in entries.items():
            if key != "_std":
                lines.append(f"  • {key}: {val}")

    lines.append("\n=== המפרט הכללי — הספר הכחול (ניווט לפי פרקים) ===")
    for chapter, desc in BLUE_BOOK.items():
        lines.append(f"  {chapter}: {desc}")

    return "\n".join(lines)
