def search_engineering_kb(query: str, top_n: int = 4) -> str:
    """
    Search the engineering knowledge base by keyword.
    Returns the most relevant sections concatenated.
    """
    q_lower = query.lower()
    scored: list[tuple[int, str, str]] = []

    for section_name, keywords in _KEYWORD_MAP.items():
        score = sum(1 for kw in keywords if kw.lower() in q_lower)
        if score > 0:
            scored.append((score, section_name, _ALL_SECTIONS[section_name]))

    scored.sort(key=lambda x: x[0], reverse=True)
    if not scored:
        return ""

    parts = []
    for _, name, content in scored[:top_n]:
        # Return only the relevant sub-sections, not full section
        parts.append(_extract_relevant_subsections(content, query))

    return "\n\n".join(p for p in parts if p)


def _extract_relevant_subsections(section_text: str, query: str) -> str:
    """Extract only the sub-sections of a knowledge base entry that match the query."""
    q_lower = query.lower()
    lines = section_text.split("\n")

    result_blocks: list[list[str]] = []
    current_block: list[str] = []
    current_score = 0

    for line in lines:
        if line.startswith("──") or line.startswith("==="):
            if current_block and current_score > 0:
                result_blocks.append((current_score, list(current_block)))
            current_block = [line]
            current_score = 0
        else:
            current_block.append(line)
            words = query.lower().split()
            for w in words:
                if len(w) > 2 and w in line.lower():
                    current_score += 1

    if current_block and current_score > 0:
        result_blocks.append((current_score, current_block))

    result_blocks.sort(key=lambda x: x[0], reverse=True)

    # Always include section header
    header = lines[0] if lines else ""
    selected = [header] if header else []
    for _, block in result_blocks[:3]:
        selected.extend(block)

    return "\n".join(selected) if selected else section_text[:1500]


def get_full_engineering_kb() -> str:
    """Return complete engineering knowledge base (for system prompts)."""
    return "\n\n".join(_ALL_SECTIONS.values())
