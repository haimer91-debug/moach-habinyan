# -*- coding: utf-8 -*-
"""
Estimator Agent — הערכת כמויות ועלויות לעבודות בנייה.
⚠️ הערכה ראשונית בלבד — אינה מחליפה תכנון מהנדס קונסטרוקציה.
"""
import anthropic
from standards.engineering_kb import COSTS_2026
from standards.professional_kb import QUANTITY_SURVEYING

# ── כללים קונסטרוקטיביים לחישוב ─────────────────────────────────────────────
_STRUCTURAL_RULES = """\
=== כללי תכנון ראשוניים — ישראל (הערכה בלבד) ===

מרפסת בטון — קנטילוור (בליטה):
• עובי לוח (h):  L/10 ≤ h ≤ L/8   [L = אורך הבליטה]
  מינימום מוחלט: 120 מ"מ
  בפועל מעגלים ל-5 ס"מ הקרוב כלפי מעלה
• חוזק בטון: B30 (C25) מינימום לחוץ  (כיתת חשיפה XC3–XC4, ת"י 118)
• כיסוי ברזל: 35–40 מ"מ לחוץ
• זיון ראשי עליון (נגד מומנט):
  - בליטה עד 1.5 מ'  → ∅10 @ 150 מ"מ
  - בליטה 1.5–2.5 מ' → ∅12 @ 130–150 מ"מ
  - בליטה 2.5–3.5 מ' → ∅12 @ 100–120 מ"מ  (או ∅14 @ 150 מ"מ)
  - בליטה >3.5 מ'    → ∅16 @ 120–150 מ"מ  (נדרש חישוב מהנדס)
• זיון חלוקה (תחתון):  ∅10 @ 200 מ"מ (בשני כיוונים)
• כמות ברזל כוללת טיפוסית: 80–120 ק"ג/מ"ר

חישוב כמויות:
  נפח בטון [מ"ק] = שטח [מ"ר] × עובי [מ']
  משקל ברזל [טון] = שטח [מ"ר] × 0.100  (100 ק"ג/מ"ר — ממוצע)
  תבנית [מ"ר] = שטח [מ"ר] × 1.10  (10% פחת)

עומסי תכנון טיפוסיים (ת"י 412):
  עומס חי מרפסת/מרפסת ציבורית: 300–500 ק"ג/מ"ר
  עומס מת (לוח + ריצוף + טיח): 400–600 ק"ג/מ"ר

גבהים ופיגומים:
  קומת קרקע עד קומה 2:  פיגום פשוט
  קומה 3 ומעלה:          פיגום תלוי / בריקאדה — עלות נוספת ₪5,000–15,000 לפרויקט

הגבלות ואזהרות:
  ⚠️ בליטה >3 מ' — נדרש תכנון מהנדס קונסטרוקציה
  ⚠️ קיר תומך/עמוד — יש לבדוק יכולת נשיאה
  ⚠️ אינה כוללת: מעקה (∅1,100–1,500/מ'), מרזבים, חיבורים לשלד קיים
"""

_SYSTEM = f"""\
אתה מהנדס כמויות (QS) ישראלי מנוסה — מתמחה בהערכת עלויות ראשוניות לעבודות בנייה.
מפקח בנייה שולח לך תיאור של עבודה עם מידות — תחשב כמויות ועלויות מייצגות.

הנחיות:
1. חלץ מהתיאור: סוג עבודה, מידות (אורך × רוחב × גובה), קומה, מפרט נוסף
2. חשב בדיוק: שטח, נפח, כמות ברזל, כמות תבנית — הראה את החשבון
3. השתמש בטבלאות שלהלן לתמחור — תן טווח (מינ'–מקס')
4. פורמט התשובה — השתמש **בדיוק** בכותרות הבאות:

📐 נתונים שנקלטו:
[חזור על המידות שהבנת — שטח, קומה, סוג]

🔩 פרמטרים קונסטרוקטיביים:
[עובי, חוזק בטון, כיסוי, זיון — עם חישוב קצר]

📦 כמויות:
[בטון מ"ק, ברזל טון, תבנית מ"ר — עם חישוב מפורט]

💰 הערכת עלויות (ללא מע"מ):
[פירוט פריט-פריט, ואז סה"כ בטווח]

📌 הערות:
[מה לא כלול, מה מומלץ לבדוק, אזהרה על תכנון מהנדס]

כללים מחייבים:
• חישוב גלוי — הראה את הנוסחה ואת המספרים
• אם מידה חסרה — הנח הנחה סבירה וציין אותה במפורש
• ענה תמיד בעברית
• אל תמציא מחירים — השתמש רק בטבלאות שמסופקות

{COSTS_2026}

{QUANTITY_SURVEYING}

{_STRUCTURAL_RULES}
"""


def estimate_work(description: str, client: anthropic.Anthropic) -> str:
    """Estimate quantities and costs for a construction task.

    description: free Hebrew text from user, e.g. "מרפסת בטון 3.5×2 מ' קומה 3"
    Returns plain Hebrew text ready for Telegram.
    """
    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=1800,
        system=_SYSTEM,
        messages=[{
            "role": "user",
            "content": (
                "הערכת כמויות ועלויות לפי הנתונים הבאים:\n\n"
                + description
            ),
        }],
    )
    return response.content[0].text.strip()
