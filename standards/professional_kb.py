def search_professional_kb(query: str, top_n: int = 3) -> str:
    """Search the professional KB by keyword matching. Returns top matching sections."""
    keywords = [w for w in query.lower().split() if len(w) > 2]

    section_scores: list[tuple[int, str, str]] = []

    for section_name, keywords_list in _KEYWORD_MAP.items():
        score = 0
        for kw in keywords:
            for map_kw in keywords_list:
                if kw in map_kw.lower() or map_kw.lower() in kw:
                    score += 2
            content = _ALL_SECTIONS.get(section_name, "")
            for line in content.split("\n"):
                if kw in line.lower():
                    score += 1
        if score > 0:
            section_scores.append((score, section_name, _ALL_SECTIONS.get(section_name, "")))

    section_scores.sort(key=lambda x: x[0], reverse=True)

    if not section_scores:
        return ""

    results = []
    for score, name, content in section_scores[:top_n]:
        results.append(content)

    return "\n\n".join(results)


def get_all_professional_kb() -> str:
    """Return the full professional KB text."""
    return "\n\n".join(_ALL_SECTIONS.values())
