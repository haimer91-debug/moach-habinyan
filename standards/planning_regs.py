def search_regulations(query: str) -> str:
    """
    Simple keyword search in the regulations text.
    Returns the most relevant paragraph(s).
    """
    keywords = query.lower().split()
    lines = PLANNING_REGULATIONS.split("\n")

    # Score each section by keyword density
    sections: list[tuple[int, list[str]]] = []
    current_section: list[str] = []
    current_score = 0

    for line in lines:
        if line.startswith("─") or line.startswith("="):
            if current_section and current_score > 0:
                sections.append((current_score, current_section[:]))
            current_section = [line]
            current_score = 0
        else:
            current_section.append(line)
            for kw in keywords:
                if kw in line.lower():
                    current_score += 1

    if current_section and current_score > 0:
        sections.append((current_score, current_section))

    sections.sort(key=lambda x: x[0], reverse=True)
    if not sections:
        return ""

    top = sections[:3]
    result_parts = []
    for score, sec_lines in top:
        result_parts.append("\n".join(sec_lines))

    return "\n\n".join(result_parts)
